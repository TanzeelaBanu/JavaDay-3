package tanzeela;

public class ExampleForLoop 
{
	public static void main(String[] args)
	{
		for(int i=1;i<=5;i++)
		{
			System.out.println("Hello");
		}
		
		// 1,2 3, 4, 5
		
		for(int i=146; i<=150;i++)
		{
			System.out.println(i);
		}

		// 5 , 4, 3 ,2 ,1
		
		for(int i =5;i>=1;i--)
		{
			System.out.println(i);
		}
		// 2, 4 , 6, 8, 10
		
		for(int j=2;j<=10;j+=2)
		{
			System.out.println(j);
		}
	}
}

